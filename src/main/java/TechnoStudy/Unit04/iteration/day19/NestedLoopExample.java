package TechnoStudy.Unit04.iteration.day19;

public class NestedLoopExample {
    public static void main(String[] args) {


        for (int i = 0; i < 3; i++) {
            System.out.println(i);

            for (int j = 0; j < 4; j++) {
                System.out.print(j+ "\t");
            }
            System.out.println();
        }

    }
}
