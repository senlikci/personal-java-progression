package TechnoStudy.Unit04.iteration.day19;

public class DecliningTriangle {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {

            for (int j = i; j < 5; j++) {

                System.out.print("* ");

            }
            System.out.println();
        }
    }
}
