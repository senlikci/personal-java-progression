package TechnoStudy.Unit03.conditionals.day13;

public class PositiveNegative {

    public static void main(String[] args) {

        int number = 0;

        if (number > 0) {
            System.out.println("positive");
        } else if (number < 0) {
            System.out.println("negative");

        } else {
            System.out.println("zero");
        }

    }

}
